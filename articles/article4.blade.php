            <p>Calling rappers out for biting flows is a touchy subject and can end up in violence. On October 21, during the Rolling Loud festival in Northern California, <a href="https://genius.com/artists/A-boogie-wit-da-hoodie" data-api_path="/artists/639521">A Boogie wit da Hoodie</a> and <a href="https://genius.com/artists/Pnb-rock" data-api_path="/artists/248458">PNB Rock</a> allegedly jumped <a href="https://genius.com/artists/Lil-b" data-api_path="/artists/455">Lil B.</a> The altercation likely stemmed from two tweets Lil B tweet sent on September 30th saying:</p>

