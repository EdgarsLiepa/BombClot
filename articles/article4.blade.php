            <p>Lil B forgave A Boogie &amp; PNB Rock immediately and they have since squashed their beef. However, B’s tweet highlights an intense debate in hip-hop: Is it okay for a flow to be co-opted by another artist?</p>

            <p>Meanwhile, <a href="https://genius.com/artists/Cardi-b" data-api_path="/artists/621678">Cardi B</a>, whose currently enjoying the success of her hit <a href="https://genius.com/Cardi-b-bodak-yellow-lyrics" data-api_path="/songs/3095483">“Bodak Yellow,”</a> used <a href="https://genius.com/artists/Kodak-black" data-api_path="/artists/162126">Kodak Black</a>’s flow from <a href="https://genius.com/Kodak-black-no-flockin-lyrics" data-api_path="/songs/570825">“No Flockin”</a> to rise to the top of the charts.</p>

