            <p>There is perhaps no issue in 2017 hip-hop more divisive than the merits of so-called “mumble rap.” The term, which is now applied to a wide swath of new artists with different styles, originated as a way to describe artists like <a href="https://genius.com/artists/Future" data-api_path="/artists/2197">Future</a> and <a href="https://genius.com/artists/Young-thug" data-api_path="/artists/20503">Young Thug</a>, whose lyrics are often initially tough to decipher. However, the tradition of focusing on melody and flow over lyrical articulation goes back much further to artists like <a href="https://genius.com/artists/Bone-thugs-n-harmony" data-api_path="/artists/64">Bone Thugs-n-Harmony</a>, <a href="https://genius.com/artists/Ol-dirty-bastard" data-api_path="/artists/32">Ol' Dirty Bastard</a>, and more. Today, “mumble rappers” like <a href="https://genius.com/artists/Lil-uzi-vert" data-api_path="/artists/217208">Lil Uzi Vert</a>, <a href="https://genius.com/artists/21-savage" data-api_path="/artists/430404">21 Savage</a>, and the aforementioned Future rule the charts, and even traditional MCs are taking notice, for better or for worse.</p>

